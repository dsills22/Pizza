public class Item {
    public Integer removeBtnId;
    public Integer rowId;
    public Double price;
    public Integer updateBtnId;
    public Integer index;

    public Integer getRemoveBtnId() {
        return removeBtnId;
    }

    public void setRemoveBtnId(Integer removeBtnId) {
        this.removeBtnId = removeBtnId;
    }

    public Integer getRowId() {
        return rowId;
    }

    public void setRowId(Integer rowId) {
        this.rowId = rowId;
    }

    public Double getPrice() {
        return price;
    }

    public void setPrice(Double price) {
        this.price = price;
    }

    public Integer getUpdateBtnId() {
        return updateBtnId;
    }

    public void setUpdateBtnId(Integer updateBtnId) {
        this.updateBtnId = updateBtnId;
    }

    public Integer getIndex() {
        return index;
    }

    public void setIndex(Integer index) {
        this.index = index;
    }
}
